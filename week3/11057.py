n = int(input())

def dp(x):
    dp = [[0 for _ in range(10)] for _ in range(x+1)]

    for i in range(10):
        dp[0][i] = 1

    for i in range(1, x+1):
        for j in range(10):
            dp[i][j] = sum(dp[i - 1][j:10]) # dp[i-1][j:10]  j ≤ index ≤ 9

    return dp[x][0]


print(dp(n)%10007)
